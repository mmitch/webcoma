sub expand($$)
{
    my $zeile = shift;
    my $lang = shift;

    if ($zeile =~ /#D?LINK:([^#]*)#/) {
	my ($link, $hash) = split /!/, $1, 2;
	if ($hash) {
	    $hash = "#$hash";
	} else {
	    $hash = "";
	}
	$zeile =~ s/#D?LINK:[^#]*#/<a href="$link.$lang.html$hash">/;
    }

    if ($zeile =~ s/#SUBTITLE:(.*):[^:]*:/<a name="$subtitlecount">$1<\/a>/) {
	$subtitlecount++;
    }

    return $zeile;
}


