sub expand($$)
{
    my $zeile = shift;
    my $lang = shift;

    if ($zeile =~ /#D?LINK:([^#]*)#/) {
	my ($link, $hash, $class) = split /!/, $1, 3;
	if ($hash) {
	    $hash = "#$hash";
	    if ($class) {
		$class=" class=\"$class\"";
	    } else {
		$class="";
	    }
	} else {
	    $hash  = "";
	    $class = "";
	}
	$zeile =~ s/#D?LINK:[^#]*#/<a href="$link.$lang.html$hash"${class}>/;
    }

    if ($zeile =~ s/#SUBTITLE:(.*):[^:]*:/<a name="$subtitlecount">$1<\/a>/) {
	$subtitlecount++;
    }

    return $zeile;
}


